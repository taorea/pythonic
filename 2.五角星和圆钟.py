import turtle

turtle.showturtle()
turtle.write("五角星")
turtle.color("green")
turtle.forward(100)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(100)

turtle.penup()
turtle.goto(200,0)
turtle.pendown()
turtle.circle(100)

turtle.penup()
turtle.goto(120,100)
turtle.write("9点")
turtle.pendown()
turtle.forward(160)
turtle.write("3点")
turtle.right(90)
turtle.penup()
turtle.goto(200,180)
turtle.write("12点")
turtle.pendown()
turtle.forward(160)
turtle.write("6点")
turtle.done()